function isUiLanguage(value: unknown): value is UiLanguage {
  return value === 'en' || value === 'zh-CN'
}

function readStoredLanguage(): UiLanguage {
  if (typeof window === 'undefined') {
    return 'en'
  }

  try {
    const stored = window.localStorage?.getItem?.(languageStorageKey)
    if (stored && isUiLanguage(stored)) {
      return stored
    }
  } catch {
    return 'en'
  }

  // Intentionally default to English to avoid surprising test and automation failures.
  // Operators can switch language via the header control; selection is persisted.
  return 'en'
}

function formatParams(template: string, params?: TranslationParams) {
  if (!params) {
    return template
  }

  return template.replace(/\{(\w+)\}/g, (_, key: string) => {
    const value = params[key]
    return value === null || value === undefined ? `{${key}}` : String(value)
  })
}

function translate(language: UiLanguage, key: string, params?: TranslationParams) {
  const template = dictionaries[language]?.[key] ?? dictionaries.en[key] ?? key
  return formatParams(template, params)
}

type I18nValue = {
  language: UiLanguage
  setLanguage: (language: UiLanguage) => void
  t: (key: string, params?: TranslationParams) => string
}

const I18nContext = createContext<I18nValue | null>(null)

export function I18nProvider(props: { children: ReactNode }) {
  const [language, setLanguageState] = useState<UiLanguage>(() => readStoredLanguage())

  const setLanguage = (next: UiLanguage) => {
    setLanguageState(next)
    if (typeof window !== 'undefined') {
      try {
        window.localStorage?.setItem?.(languageStorageKey, next)
      } catch {
        return
      }
    }
  }

  const t = useMemo(() => {
    return (key: string, params?: TranslationParams) => translate(language, key, params)
  }, [language])

  const value = useMemo<I18nValue>(() => ({ language, setLanguage, t }), [language, t])

  useEffect(() => {
    if (typeof document === 'undefined') {
      return
    }

    document.documentElement.lang = language
  }, [language])

  return <I18nContext.Provider value={value}>{props.children}</I18nContext.Provider>
}

export function useI18n() {
  const value = useContext(I18nContext)
  if (!value) {
    throw new Error('useI18n must be used within an I18nProvider.')
  }
  return value
}
